test_that("invalid geometry type code errors", {
  skip_if_not(has_arrow_with_extension_type())

  bad_point <- as.raw(c(0x01,
                        0x01, 0x01, 0x01, 0x01,
                        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x3e, 0x40,
                        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x24, 0x40))

  array <- arrow::Array$create(list(bad_point), arrow::binary())
  arr_wkb <- narrow::as_narrow_array(array)
  arr_wkb$schema <- geoarrow_schema_wkb()
  expect_error(wk::wk_void(arr_wkb), "Unrecognized geometry type")

  array <- arrow::Array$create(list(as.raw(0x01)), arrow::binary())
  arr_wkb <- narrow::as_narrow_array(array)
  arr_wkb$schema <- geoarrow_schema_wkb()
  expect_error(wk::wk_void(arr_wkb), "Unexpected end of buffer")
})

test_that("bad arrays error", {
  arr_wkb <- narrow::narrow_array(
    geoarrow_schema_wkb(format = "w:0"),
    narrow::narrow_array_data(buffers = list(raw(1), raw(1))),
    validate = FALSE
  )
  expect_error(wk::wk_void(arr_wkb), "Expected fixed-width binary to have width > 0")

  arr_wkb <- narrow::narrow_array(
    narrow::narrow_schema("i", metadata = list("ARROW:extension:name" = "geoarrow.wkb")),
    narrow::narrow_array_data(buffers = list(NULL, integer())),
    validate = FALSE
  )
  expect_error(wk::wk_void(arr_wkb), "Unsupported storage type for extension geoarrow.wkb")
})

